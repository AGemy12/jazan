function flyToPlace(place) {

    const lat = place.geometry.coordinates[1];
    const lng = place.geometry.coordinates[0];

    map.flyTo([lat, lng], 14, {
        duration: 1
    })

    setTimeout(() => {

        L.popup({ closeButton: false, offset: L.point(0, -35)})
        .setLatLng([lat, lng])
        .setContent(makePopupContent(place))    
        .openOn(map);

    }, 1500);

}


