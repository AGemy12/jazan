var geocoder = L.Control.geocoder({
    geocoder: L.Control.Geocoder.nominatim(),
    
    autoCollapse: false
  }).on('markgeocode', function (e) {
    var latlng = e.geocode.center;

    if (!map.startLatLng) {
        map.startLatLng = latlng;
    } else {
        var endLatLng = latlng;
        addRoutingControl(map.startLatLng, endLatLng);
        map.startLatLng = null;
    }
  }).addTo(map);

  geocoder._container.firstChild.click();

  function addRoutingControl(startLatLng, endLatLng) {
    var control = L.Routing.control({
      waypoints: [
        startLatLng,
        endLatLng
      ],
      routeWhileDragging: true,
      lineOptions: {
        styles: [{color: 'rgba(173 33 65 / 95%)', opacity: 1, weight: 3}]
      },
      }).addTo(map);
    
    var waypointsBounds;
    
    // Check if the getPlan and getWaypointsBounds functions are available
    if (control.getPlan && control.getPlan().getWaypointsBounds) {
      waypointsBounds = control.getPlan().getWaypointsBounds();
    } else {
      // Fallback or alternative approach if the functions are not available
      // You may need to adapt this based on your specific use case
      waypointsBounds = L.latLngBounds(startLatLng, endLatLng);
    }

    map.fitBounds(waypointsBounds);

}


