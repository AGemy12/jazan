function generationList () {
      var listsContainer = document.getElementById('lists');
        placeList.forEach(function (place, index) {
            var listItem = document.createElement('li');
            var listLink = document.createElement('a');
            var paragraph = document.createElement('p');
            var liContainer = document.createElement('div');

            listLink.href = '#';
            listLink.innerText = place.properties.name;
            listLink.onclick = function (event) {
                event.preventDefault(); // Prevent the link from navigating
                event.stopPropagation();
                toggleParagraph(index + 1);
                flyToPlace(place);
            };

            listLink.className = 'place-link';
            liContainer.className = 'place-info-container';

            paragraph.id = 'paragraph' + (index + 1);
            paragraph.className = 'hidden';
            paragraph.innerText = place.properties.text;

            liContainer.appendChild(listLink)
            liContainer.appendChild(paragraph)
            listItem.appendChild(liContainer);
            listsContainer.appendChild(listItem);
        });
        
        // Toggle the visibility of the clicked paragraph
        function toggleParagraph(paragraphNumber) {
          var clickedParagraph = document.getElementById('paragraph' + paragraphNumber);

          // Toggle the visibility of the clicked paragraph
          if (clickedParagraph) {
              if (clickedParagraph.classList.contains('visible')) {
                  clickedParagraph.classList.remove('visible');
              } else {
                  closeAllParagraphs();
                  clickedParagraph.classList.add('visible');
              }
          }
      }

      // Close all paragraphs when clicking anywhere on the document
      document.addEventListener('click', function (event) {
          var clickedElement = event.target;
          if (clickedElement.tagName.toLowerCase() !== 'a') {
              closeAllParagraphs();
          }
      });

      // Close all paragraphs
      function closeAllParagraphs() {
          var paragraphs = document.querySelectorAll('.visible');
          paragraphs.forEach(function (paragraph) {
              paragraph.classList.remove('visible');
          });
      }        
}

generationList();

